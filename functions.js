/**
 * Function name: numberToString - Converts a number to a string
 * @param  {type: Number} n
 * @return {type: String}
 */
function numberToString( num ) {
  if ((typeof num !== 'number') || ( isNaN(num) )){
    throw new TypeError('Please give me a number!');
  }
  return num.toString();
}

